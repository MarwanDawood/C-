function [mapName, relativePathToMapFile, found] = getBlockHelpMapNameAndPath(block_type)
%  Returns the mapName and the relative path to the maps file for this block_type

% Internal note:
%   First column is the "System object name", corresponding to the block,
%   Second column is the anchor ID, the doc uses for the block.
%   For core blocks, the first column is the 'BlockType'.

% Copyright 2016 The MathWorks, Inc.

blks = {...
    'raspi.internal.hts221Block'           		     'libshhts221'           ;...
    'raspi.internal.lps25hBlock'           		     'libshlps25h'           ;...
    'raspi.internal.lsm9ds1Block'           	     'libshlsm9ds1'           ;...
    'raspi.internal.JoystickBlock'           	     'libshjoystick'           ;...
    'raspi.internal.LEDMatrixBlock'           	     'libshledmatrix'           ;...
	'raspi.internal.TCPReceive'                      'libtcpipreceive'          ;...
	'raspi.internal.TCPSend'                         'libtcpipsend'                 ;...
    'codertarget.raspi.internal.I2CMasterRead'       'libi2cmasterread'           ;...
    'codertarget.raspi.internal.I2CMasterWrite'      'libi2cmasterwrite'           ;...
    'codertarget.raspi.internal.SCIRead'             'libserialread'           ;...
    'codertarget.raspi.internal.SCIWrite'            'libserialwrite'           ;...
    'codertarget.raspi.internal.SPIMasterTransfer'   'libspimastertransfer'           ;...
    'codertarget.raspi.internal.SPIRegisterRead'     'libspiregisterread'           ;...
    'codertarget.raspi.internal.SPIRegisterWrite'    'libspiregisterwrite'           ;...
    'codertarget.raspi.internal.AudioFileRead'       'libraspiaudiofileread'        ;...
    'codertarget.linux.blocks.ALSAAudioCapture'      'libalsaaudiocapture'          ;...
    'codertarget.linux.blocks.ALSAAudioPlayback'     'libalsaaudioplayback'         ;...
    'codertarget.linux.blocks.ThingSpeakRead'        'libthingspeakread'            ;...
    'codertarget.linux.blocks.MQTTSubscribe'         'liblinuxmqttsubscribe'        ;...
    'codertarget.linux.blocks.MQTTPublish'           'liblinuxmqttpublish'          ;...
    };
relativePathToMapFile = '';
found = false;
% See whether or not the block is a Support Package System Block
i = strcmp(block_type, blks(:,1));

if ~any(i)
    mapName = 'User Defined';
else
    raspidoc =  codertarget.internal.raspberrypi;
    raspidocroot = raspidoc.getDocRoot();
    relativePathToMapFile = fullfile(raspidocroot, 'helptargets.map');
    found = 'fullpath';
    mapName = blks(i,2);
end

